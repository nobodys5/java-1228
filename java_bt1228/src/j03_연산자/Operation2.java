package j03_연산자;

public class Operation2 {
	public static void main(String[] args) {

		// 증감연산자
		int num = 50;
		System.out.println(num);
		num++;
		System.out.println(num);
		++num;
		System.out.println(num);
		
		int num2 = 50;
		System.out.println(num2);
		System.out.println(++num2);
		System.out.println(num2);
		System.out.println(num2++);
		System.out.println(num2);
		
		System.out.println();
		num2 = 50;
		System.out.println(num2);
		System.out.println(--num2);
		System.out.println(num);
		System.out.println(num--);
		System.out.println(num);
	}

}
